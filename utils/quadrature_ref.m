function I = quadrature_ref(f)
%QUADRATURE Summary of this function goes here
%   Detailed explanation goes here
persistent xq yq omegaq
if isempty(xq)
    q=sqrt(15);
    xq=[1/3,...
        (6+q)/21,...
        (9-2*q)/21,...
        (6+q)/21,...
        (6-q)/21,...
        (9+2*q)/21,...
        (6-q)/21];
    yq=[1/3,...
        (6+q)/21,...
        (6+q)/21,...
        (9-2*q)/21,...
        (6-q)/21,...
        (6-q)/21,...
        (9+2*q)/21];
    omegaq=[9/80,...
        (155+q)/2400,...
        (155+q)/2400,...
        (155+q)/2400,...
        (155-q)/2400,...
        (155-q)/2400,...
        (155-q)/2400];
end

I=0;
for i=1:7
    I=I+omegaq(i)*f([xq(i);yq(i)]);
end

end
